// Workers types declare `KeyUsage` only as part of the DOM lib. We narrow the
// union explicitly so this file compiles under `lib: ES2023` without dragging
// the DOM types in (which would shadow some Workers globals).
type HmacKeyUsage = "sign" | "verify";

async function importHmacKey(
  secret: string,
  usages: HmacKeyUsage[],
): Promise<CryptoKey> {
  return crypto.subtle.importKey(
    "raw",
    new TextEncoder().encode(secret),
    { name: "HMAC", hash: "SHA-256" },
    false,
    usages,
  );
}

/**
 * base64url = base64 with `+ → -`, `/ → _`, no padding. Workers runtime has
 * globalThis.btoa for the base64 step; we operate on a binary string built
 * from the byte array (the canonical pattern; using `btoa` directly on a
 * UTF-8 string would mangle anything > 0x7F, hence the manual byte-stringify).
 */
function base64urlEncode(bytes: Uint8Array): string {
  let bin = "";
  for (let i = 0; i < bytes.length; i++) bin += String.fromCharCode(bytes[i]!);
  const b64 = btoa(bin);
  return b64.replace(/\+/g, "-").replace(/\//g, "_").replace(/=+$/, "");
}

function base64urlDecode(input: string): Uint8Array {
  const b64 = input.replace(/-/g, "+").replace(/_/g, "/");
  const padded = b64 + "=".repeat((4 - (b64.length % 4)) % 4);
  const bin = atob(padded);
  const bytes = new Uint8Array(bin.length);
  for (let i = 0; i < bin.length; i++) bytes[i] = bin.charCodeAt(i);
  return bytes;
}
